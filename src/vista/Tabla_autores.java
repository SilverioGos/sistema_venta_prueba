    private void TablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaMouseClicked
        int fila=Tabla.getSelectedRow();
        if (fila == -1){
            JOptionPane.showMessageDialog(this, "Debe seleccionar una fila");
        } else {
            cod_au = Integer.parseInt(Tabla.getValueAt(fila,0).toString());
            String nombre = Tabla.getValueAt(fila,1).toString();
            String apellido = Tabla.getValueAt(fila,2).toString();
            String nacionalidad = Tabla.getValueAt(fila,3).toString();
            txtNombre.setText(nombre);
            txtApellido.setText(apellido);
            txtNacionalidad.setText(nacionalidad);
        }
    }//GEN-LAST:event_TablaMouseClicked
    
    void agregar(){
        String nombre=txtNombre.getText();
        String apellido=txtApellido.getText();
        String nacionalidad=txtNacionalidad.getText();
        Object[]ob= new Object [3];
        ob[0]=nombre;
        ob[1]=apellido;
        ob[2]=nacionalidad;
        dao.add(ob);
    }
    
    void actualizar(){
        int fila=Tabla.getSelectedRow();
        if (fila == -1){
            JOptionPane.showMessageDialog(this,"Debe seleccionar una fila");
        } else {
        String nombre=txtNombre.getText();
        String apellido=txtApellido.getText();
        String nacionalidad=txtNacionalidad.getText();
        Object[]obj= new Object [4];
        obj[0]=nombre;
        obj[1]=apellido;
        obj[2]=nacionalidad;
        obj[3]=cod_au;
        dao.actualizar(obj);
        }
    }
    
    void eliminar(){
        int fila=Tabla.getSelectedRow();
        int ids = Integer.parseInt(Tabla.getValueAt(fila,0).toString());
        if (fila == -1){
            JOptionPane.showMessageDialog(this,"Debe seleccionar una fila");
        } else {
            dao.eliminar(ids);
        }
    }
    
    void nuevo(){
        txtNombre.setText("");
        txtApellido.setText("");
        txtNacionalidad.setText("");
    }
    void limpiarTabla(){
        for (int i=0; i< modelo.getRowCount(); i++){
            modelo.removeRow(i);
            i=i-1;
        }
    }

