    void listar(){
        List<autores> lista=dao.listar();
        modelo=(DefaultTableModel)Tabla.getModel();
        Object[]ob=new Object[4];
        for (int i=0; i<lista.size(); i++){
            ob[0]=lista.get(i).getCod_au();
            ob[1]=lista.get(i).getNom_autor();
            ob[2]=lista.get(i).getApe_autor();
            ob[3]=lista.get(i).getNacionalidad();
            modelo.addRow(ob);
        }
        Tabla.setModel(modelo);
    }

